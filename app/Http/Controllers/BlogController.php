     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        // return 'hello world';

        $years = Blog::query()
            ->select('id', 'published_at', 'author_id', 'slug', 'title')
            ->with('author:id,name')
            ->latest('published_at')
            ->get()
            ->groupBy(fn ($blog) => \Carbon\Carbon::parse($blog->published_at)->year);


        return view('blogs.index', ['years' => $years]);
    }

